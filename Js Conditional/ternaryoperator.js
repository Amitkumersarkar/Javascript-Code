// ternary operator
// structure of ternary
// steps1 : conditions
// steps2 : (?) sign (do something when condition true)
// steps3 : then (:) sign (do something when condition false)

const age = 17;
if (age >= 18) {
    console.log('your able to give vote');
}
else {
    console.log('your not able to give vote');
}

// ternary condition apply here

age >= 17 ? console.log('vote allowed') : console.log('not allowed')
